
class Employee {

    constructor(name, id, email) {
        // name !== undefined ? this.name = name : console.log('Name cannot be empty');
        this.name = name;
        this.id = id;
        this.email = email;
    }

    getName() {
        return this.name;
    }

    getId() {
        return this.id;
    }

    getEmail() {
        return this.email;
    }

    getRole() {
        return "Employee";
    }
}

module.exports = Employee;